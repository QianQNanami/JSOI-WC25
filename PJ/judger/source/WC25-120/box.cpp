#include<bits/stdc++.h>
#define ll long long
using namespace std;
ll a,b,c,d,e,f;
ll ans;
int main()
{
	while(1)
	{
		scanf("%lld%lld%lld%lld%lld%lld",&a,&b,&c,&d,&e,&f);
		if(a==0&&b==0&&c==0&&d==0&&e==0&&f==0)
		return 0;
		ans+=f;
		if(e>=0)
		{
			ans+=e;
			a-=11;
			if(a<0)
			a=0;
		}
		if(d>=0)
		{
			ans+=d;
			if(b>=5*d)
			{
				b-=5*d;
			}
			else
			{
				b=0;
				a-=(5*d-b)*4;
				if(a<0)
				a=0;
			}
		}
		if(c>=0)
		{
			if(c>=4)
			{
				ans+=(c/4),c=c%4;
			}
			if(c==3)
			{
				c=0;
				ans++;
				if(b>=0)
				{
					b--;
					if(a>=0)
					a-=5;
					if(a<0)
					a=0;
				}
				if(a>0)
				{
					a-=9;
					if(a<0)
					a=0;
				}
			}
			if(c==2)
			{
				if(b>=3)
				{
					b-=3;
					a-=6;
					if(a<0)
					a=0;
				}
				else if(b>0)
				{
					a-=(18-b*4);
					b=0;
					if(a<0)
					a=0;
				}
				else if(a>0)
				{
					a-=18;
					if(a<0)
					a=0;
				}
				ans++;
			}
			if(c==1)
			{
				if(b>=5)
				{
					b-=5;
					a-=7;
					if(a<0)
					a=0;
				}
				else if(b>0)
				{
					a-=(27-b*4);
					b=0;	
					if(a<0)
					a=0;
				}
				else if(a>0)
				{
					a-=27;
					if(a<0)
					a=0;
				}
				ans++;
			}
		}
		if(b>=0)
		{
			if(b>=9)
			ans+=(b/9),b=b%9;
			if(b>0)
			{
				a-=(36-b*4);
				ans++;
				b=0;
				if(a<0)
				a=0;
			}
		}
		if(a>0)
		{
			if(a%36==0)
			ans+=a/36;
			else
			ans+=a/36+1;
		}
		cout<<ans<<endl;
		ans=0;
	}
	return 0;
}
